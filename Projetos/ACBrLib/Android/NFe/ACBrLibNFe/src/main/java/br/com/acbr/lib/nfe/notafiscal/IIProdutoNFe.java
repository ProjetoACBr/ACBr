package br.com.acbr.lib.nfe.notafiscal;

public class IIProdutoNFe {
    private Double vBC;
    private Double vDespAdu;
    private Double vII;
    private Double vIOF;

    public IIProdutoNFe() {
    }

    public Double getVBC() {
        return vBC;
    }

    public void setVBC(Double vBC) {
        this.vBC = vBC;
    }

    public Double getVDespAdu() {
        return vDespAdu;
    }

    public void setVDespAdu(Double vDespAdu) {
        this.vDespAdu = vDespAdu;
    }

    public Double getVII() {
        return vII;
    }

    public void setVII(Double vII) {
        this.vII = vII;
    }

    public Double getVIOF() {
        return vIOF;
    }

    public void setVIOF(Double vIOF) {
        this.vIOF = vIOF;
    }
}
