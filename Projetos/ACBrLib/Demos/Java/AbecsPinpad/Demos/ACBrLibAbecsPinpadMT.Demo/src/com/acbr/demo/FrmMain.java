    private void btnCEXActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCEXActionPerformed
        int TimeOutCEX = (int)nudTimeOutCEX.getValue();
        if (ckbVerifyKey.isSelected()) {
            try {
                abecsPinpad.DEX("Press Key");
                String ret = abecsPinpad.CEX(true, false, false, false, false, TimeOutCEX);
                rtbRespostaCEX.append(ret);
                abecsPinpad.DEX("");
            } catch (Exception ex) {
                Logger.getLogger(FrmMain.class.getName()).log(Level.SEVERE, null, ex);
            }
        } else if (ckbVerifyMagnetic.isSelected()) {
            try {
                abecsPinpad.DEX("Swipe the card");
                String ret = abecsPinpad.CEX(false, true, false, false, false, TimeOutCEX);
                rtbRespostaCEX.append(ret);
                abecsPinpad.DEX("");
            } catch (Exception ex) {
                Logger.getLogger(FrmMain.class.getName()).log(Level.SEVERE, null, ex);
            }
        } else if (ckbVerifyICCInsertion.isSelected()) {
            try {
                abecsPinpad.DEX("Insert card");
                String ret = abecsPinpad.CEX(false, false, true, false, false, TimeOutCEX);
                rtbRespostaCEX.append(ret);
                abecsPinpad.DEX("");
            } catch (Exception ex) {
                Logger.getLogger(FrmMain.class.getName()).log(Level.SEVERE, null, ex);
            }
        } else if (ckbVerifyICCRemoval.isSelected()){
            try {
                abecsPinpad.DEX("Remove card");
                String ret = abecsPinpad.CEX(false, false, false, true, false, TimeOutCEX);
                rtbRespostaCEX.append(ret);
                abecsPinpad.DEX("");
            } catch (Exception ex) {
                Logger.getLogger(FrmMain.class.getName()).log(Level.SEVERE, null, ex);
            }
        } else if (ckbVerifyCTLSPresence.isSelected()){
            try {
                abecsPinpad.DEX("Bring card closer");
                String ret = abecsPinpad.CEX(false, false, false, false, true, TimeOutCEX);
                rtbRespostaCEX.append(ret);
                abecsPinpad.DEX("");
            } catch (Exception ex) {
                Logger.getLogger(FrmMain.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_btnCEXActionPerformed

