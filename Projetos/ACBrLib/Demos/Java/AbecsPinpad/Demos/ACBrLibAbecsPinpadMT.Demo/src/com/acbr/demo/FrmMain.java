    private void btnArqLogActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnArqLogActionPerformed
        JFileChooser chooser = new JFileChooser();
        OpenFileFilter filter = new OpenFileFilter("log", "Log File (*.log)");
        chooser.addChoosableFileFilter(filter);
        chooser.setFileFilter(filter);

        int returnVal = chooser.showSaveDialog(rootPane);
        if (returnVal == JFileChooser.APPROVE_OPTION) {
            txtArqLogPinpad.setText(chooser.getSelectedFile().getPath());
        }
    }//GEN-LAST:event_btnArqLogActionPerformed

    private void formWindowOpened(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowOpened
        try {
            abecsPinpad = new ACBrAbecsPinpad();

            loadConfig();

            Path path = Paths.get(System.getProperty("user.dir"), "Logs");
            if (!Files.isDirectory(path)) {
                path.toFile().mkdirs();
            }

            abecsPinpad.configGravarValor(ACBrSessao.Principal, "LogNivel", NivelLog.logParanoico);
            abecsPinpad.configGravarValor(ACBrSessao.Principal, "LogPath", path);
            abecsPinpad.configGravar();
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(rootPane, ex);
        }
    }//GEN-LAST:event_formWindowOpened

