    private void toogleActive() throws Exception {
        if ("Ativar".equals(btnAtivar.getText())) {
            abecsPinpad.ativar();
            btnAtivar.setText("Desativar");
        } else {
            abecsPinpad.desativar();
            btnAtivar.setText("Ativar");
        }
    }

    private void loadConfig(){
        try
        {
            abecsPinpad.configLer();
            
            cmbPortaPinpad.setSelectedIndex(Integer.parseInt(abecsPinpad.configLerValor(ACBrSessao.AbecsPinpad, "PortaPinpad")));
            nudTimeOutPinpad.setValue(Integer.parseInt(abecsPinpad.configLerValor(ACBrSessao.AbecsPinpad, "TimeOut")));
            txtArqLogPinpad.setText(abecsPinpad.configLerValor(ACBrSessao.AbecsPinpad, "LogFile"));
            cmbMsgAlign.setSelectedIndex(Integer.parseInt(abecsPinpad.configLerValor(ACBrSessao.AbecsPinpad, "MsgAlign")));
            ckbWordWrap.setSelected(abecsPinpad.configLerValor(ACBrSessao.AbecsPinpad, "MsgWordWrap").equals("1"));
        }
        catch (Exception ex)
        {
            Logger.getLogger(FrmMain.class.getName()).log(Level.SEVERE, null, ex); 
        }
    }

    private void saveConfig() {
        try 
        {
            abecsPinpad.configGravarValor(ACBrSessao.AbecsPinpad, "PortaPinpad", cmbPortaPinpad.getSelectedIndex());
            abecsPinpad.configGravarValor(ACBrSessao.AbecsPinpad, "TimeOut", nudTimeOutPinpad.getValue().toString());
            abecsPinpad.configGravarValor(ACBrSessao.AbecsPinpad, "LogFile", txtArqLogPinpad.getText());
            abecsPinpad.configGravarValor(ACBrSessao.AbecsPinpad, "MsgAlign", cmbMsgAlign.getSelectedIndex());
            abecsPinpad.configGravarValor(ACBrSessao.AbecsPinpad, "MsgWordWrap", ckbWordWrap.isSelected()? "1" : "0");
            
            abecsPinpad.configGravar();
        }
        catch (Exception ex) 
        {
            Logger.getLogger(FrmMain.class.getName()).log(Level.SEVERE, null, ex);   
        }
    }

