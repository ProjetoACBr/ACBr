    private void loadConfig() {
        try {
            acbrMDFe.configLer();
            cmbVersao.setSelectedIndex(Integer.parseInt(acbrMDFe.configLerValor(ACBrSessao.MDFe, "VersaoDF")));
            cmbCrypt.setSelectedIndex(Integer.parseInt(acbrMDFe.configLerValor(ACBrSessao.DFe, "SSLCryptLib")));            
            cmbHttp.setSelectedIndex(Integer.parseInt(acbrMDFe.configLerValor(ACBrSessao.DFe, "SSLHttpLib")));    
            cmbXmlSign.setSelectedIndex(Integer.parseInt(acbrMDFe.configLerValor(ACBrSessao.DFe, "SSLXmlSignLib")));
            txtCertPath.setText(acbrMDFe.configLerValor(ACBrSessao.DFe, "ArquivoPFX"));
            txtCertPassword.setText(acbrMDFe.configLerValor(ACBrSessao.DFe, "Senha"));
            txtCertNumero.setText(acbrMDFe.configLerValor(ACBrSessao.DFe, "NumeroSerie"));
            txtSchemaPath.setText(acbrMDFe.configLerValor(ACBrSessao.MDFe, "PathSchemas"));
            cmbUfDestino.setSelectedItem(acbrMDFe.configLerValor(ACBrSessao.DFe, "UF"));
            
            String ambiente = acbrMDFe.configLerValor(ACBrSessao.MDFe, "Ambiente");            
            rdbHomologacao.setSelected("1".equals(ambiente));
            rdbProducao.setSelected("0".equals(ambiente));
                        
            cmbSSlType.setSelectedIndex(Integer.parseInt(acbrMDFe.configLerValor(ACBrSessao.MDFe, "SSLType"))); 
            nudTimeOut.setValue(Integer.parseInt(acbrMDFe.configLerValor(ACBrSessao.MDFe, "Timeout")));
            txtProxyServidor.setText(acbrMDFe.configLerValor(ACBrSessao.Proxy, "Servidor"));
            String Value = acbrMDFe.configLerValor(ACBrSessao.Proxy, "Porta");
            if(Value.matches("-?\\d+"))
                nudProxyPorta.setValue(Integer.parseInt(Value));
            txtProxyUsuario.setText(acbrMDFe.configLerValor(ACBrSessao.Proxy, "Usuario"));
            txtProxySenha.setText(acbrMDFe.configLerValor(ACBrSessao.Proxy, "Senha"));
            txtNome.setText(acbrMDFe.configLerValor(ACBrSessao.Email, "Nome"));
            txtEmail.setText(acbrMDFe.configLerValor(ACBrSessao.Email, "Conta"));
            txtUsuario.setText(acbrMDFe.configLerValor(ACBrSessao.Email, "Usuario"));
            txtSenha.setText(acbrMDFe.configLerValor(ACBrSessao.Email, "Senha"));
            txtHost.setText(acbrMDFe.configLerValor(ACBrSessao.Email, "Servidor"));
            Value = acbrMDFe.configLerValor(ACBrSessao.Email, "Porta");
            if(Value.matches("-?\\d+"))
                nudPorta.setValue(Integer.parseInt(Value));          
            ckbSSL.setSelected(acbrMDFe.configLerValor(ACBrSessao.Email, "SSL").equals("1"));            
            ckbTLS.setSelected(acbrMDFe.configLerValor(ACBrSessao.Email, "TLS").equals("1"));

        } catch (Exception ex) {
            Logger.getLogger(FrmMain.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

